void ctrl_loop(void)
{
	time = routine++ * SAMP_TIME;
	cnt = __HAL_TIM_GET_COUNTER(&htim3);
	pos = ENC_pos(e, cnt) + MTR_INIT_POS;
	vel = ENC_vel(e, pos);
	pid_control(u, pos, &tau);
	motor_pwm(tau);
//	send_Data(time, pos, vel, tau);
	sprintf(message, "%.3f, %.3f, %.3f, %.3f\r\n", time, pos, vel, tau);
	HAL_UART_Transmit_DMA(&huart2, (uint8_t *)message, strlen((char *)message));
}
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if (htim == &htim4)
	{
		ctrl_loop();
	}
}
